					<h2 class="titulo">Bestia</h2>
					<p>
						<strong>BESTIA</strong> es un festival internacional de música que incluye, desde su primera edición, actividades visuales, gráficas, sonoras en torno a una estética que está atravesada por diferentes vertientes, sea por el punk, hardcore, metal, o la distorsión.
					</p>
					<p>
						En el festival participan músicos nacionales y extranjeros cuyas propuestas han encontrado eco en el ámbito internacional, así como talento emergente cuyos asaltos sónicos demuestran que es posible olvidarse de los límites y de los “buenos modales musicales” para crear un universo sonoro que llama a un público sensible y exigente.
					</p>
					<p>
						En <strong>BESTIA</strong> existe el interés por crear experiencias sonoras en vivo, en las que el mundo del mainstream metalero no se suele arriesgar por encontrarse lejos de su zona de confort. Por ello la programación de <strong>BESTIA</strong> da cabida a bandas y proyectos que difícilmente pueden ser vistos en otros escenarios de nuestro país, porque sus sonidos se resisten a ser delimitados por etiquetas y apuestan más por difuminar las líneas divisorias entre diferentes géneros dando cabida a la improvisación y al uso del “ruido” como materia prima.
					</p>
					<p>
						La vocación de <strong>BESTIA</strong> es promover estas propuestas y darles su propia dimensión. Todo esto como respuesta a un mundo y a un país estridentes, frente a los cuales esta música no puede dejar de reaccionar, golpeando, a su manera, los fundamentos de lo establecido.
					</p>
