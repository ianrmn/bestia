							<img src="images/patton.png">
							<div class="texto-artista">
								<p>
									<strong>(California, 1968)</strong> Desde sus días con Mr. Bungle -una banda sui generis que pudo firmar un contrato con un sello disquero en los noventa-, Mike Patton se volvió una figura de culto en la arena de la música alternativa, culto que se consolidó al máximo cuando se puso al frente de Faith No More.
								</p>
								<p>
									Pero Patton es algo más que un ícono del rock alternativo. Su interés por emplear la voz como instrumento, desarrollando y ampliando diversas técnicas, lo han convertido en uno de los vocalistas más dinámicos que igual puede figurar en una banda de alto decibelaje como Phantomas o crear un disco como Adult Themes for Voices empleando su voz —procesada electrónicamente— como una fuente de sonido desconcertante que confronta en todo momento al escucha.
								</p>
								<p>
									Patton forma parte de Moonchild desde su primera encarnación como trío, haciéndose cargo, por supuesto, de la voz.
