							<img src="images/medeski.png">
							<div class="texto-artista">
								<p>
									<strong>(Kentucky, 1965)</strong> Al intrincado rompecabezas que es el paisaje jazzístico de Nueva York le faltaría una pieza central si sustrajéramos al trío que John Medeski encabeza desde 1991, como organista y tecladista, junto con el bajista Cris Wood y el baterista Billy Martin.
