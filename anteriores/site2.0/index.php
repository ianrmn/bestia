						<h3 class="subtitulo">Concierto</h3>
						<h4 class="moonchild">MOONCHILD</h4>
						<h5>moonchild en méxico. celebrando el 60° aniversario de john zorn</h5>
						<p>
							Dedicada a Antonin Artaud, literato surrealista, Edgard Varése, compositor norteamericano, y al ocultista inglés Aleister Crowley, la primer aparición discográfica de Moonchild se dio en mayo de 2006. En ese disco homónimo aparecen los nombres de Zorn como productor, compositor, conductor y arreglista y la de tres colaboradores constantes en la fructífera carrera del neoyorquino: Trevor Dunn —al bajo—, Joey Baron —en la batería— y Mike Patton —en la voz—.
						</p>
						<p>
							El álbum condensa la obsesión de John Zorn por la música extrema –léase hardcore, grindcore y metal- y por la improvisación maniaca capaz de caer siempre en una red que le dé coherencia y congruencia, haciendo así referencia a territorios que el músico y compositor domina a la perfección.
						</p>
						<p>
							Y como suele ser costumbre en su carrera como compositor y arreglista, Zorn no pierde en su mente, ni por un segundo, las capacidades de los tres músicos que lo acompañan, tanto en la concepción del proyecto como en la creación de las once piezas que integran el disco, lo cual le permite no sólo explotarlos al máximo, sino forzarlos a que una vez más, vayan más allá de sus límites.
						</p>
						<p>
							Este ensamble alcanza momentos de intensidad tales, quizás sólo comparables a los que ya se han vivido en Naked City o Pain Killer. Sin embargo sus diferentes elementos crean una trama que se siente a un mismo tiempo más elástica y más calculada, la cual seguramente por la experiencia de Zorn y la suma de los ejecutantes se ha vuelto aquí más consistente.
						</p>
						<p>
							Ese mismo trío ha sido la base para los álbumes Astronome (2006) - Six Litanies For Heliogabalus (2007) —al cual se suman Ikue Mori y Jamie Saft—, The Crucible (2008) —aumentado con Zorn al saxofón y Marc Ribot en una segunda guitarra—, Ipsissimus, con Ribot de nuevo y finalmente Templars- In Sacred Blood, donde se agrega al órgano John Medeski, configurando el cuarteto que se presenta en vivo en esta ocasión.
						</p>
						<p>
							La presentación de Moonchild en México es triplemente importante: inaugura la existencia de BESTIA Festival y con ello se suma a los festejos que este 2013 celebran el 60 aniversario de John Zorn, los cuales han tenido lugar en sitios tan importantes como el Museo Guggenheim y el MET de Nueva York. El ciclo de la gira de Moonchild culmina como el último de los festejos en la ciudad de México.
						</p>
